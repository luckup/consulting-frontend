/**
 * Upload cdn-upload/ to Vercel Blob (public store).
 *
 * Setup:
 *   1. Create public Blob store: https://vercel.com/moonsoftsnet/~/stores/blob/store_pPIvg8AEUNbGNW4T
 *   2. Connect store to your Vercel project → copies BLOB_READ_WRITE_TOKEN
 *   3. Add token to frontend/.env.local:
 *        BLOB_READ_WRITE_TOKEN=vercel_blob_rw_...
 *      Or run from frontend/: npx vercel env pull
 *   4. npm run cdn:prepare   (if cdn-upload/ is missing)
 *   5. npm run vercel:blob:upload
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { put } from '@vercel/blob'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const bundleDir = path.join(root, 'cdn-upload')

function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return
  for (const line of fs.readFileSync(filePath, 'utf8').split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eq = trimmed.indexOf('=')
    if (eq === -1) continue
    const key = trimmed.slice(0, eq).trim()
    let value = trimmed.slice(eq + 1).trim()
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }
    if (!process.env[key]) process.env[key] = value
  }
}

loadEnvFile(path.join(root, '.env.local'))
loadEnvFile(path.join(root, '.env'))

const token = process.env.BLOB_READ_WRITE_TOKEN?.trim()
if (!token) {
  console.error(
    '\nMissing BLOB_READ_WRITE_TOKEN.\n\n' +
      '  • Vercel dashboard → Storage → your Blob store → .env.local tab\n' +
      '  • Or: cd frontend && npx vercel link && npx vercel env pull\n\n' +
      'Add to frontend/.env.local:\n  BLOB_READ_WRITE_TOKEN=vercel_blob_rw_...\n',
  )
  process.exit(1)
}

if (!fs.existsSync(bundleDir)) {
  console.error('cdn-upload/ not found. Run: npm run cdn:prepare')
  process.exit(1)
}

function walkFiles(dir, base = dir) {
  const out = []
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      out.push(...walkFiles(full, base))
    } else {
      out.push({
        full,
        pathname: path.relative(base, full).replace(/\\/g, '/'),
      })
    }
  }
  return out
}

function contentTypeFor(pathname) {
  if (pathname.endsWith('.png')) return 'image/png'
  if (pathname.endsWith('.jpg') || pathname.endsWith('.jpeg')) return 'image/jpeg'
  if (pathname.endsWith('.webp')) return 'image/webp'
  return 'application/octet-stream'
}

const files = walkFiles(bundleDir)
if (files.length === 0) {
  console.error('cdn-upload/ is empty.')
  process.exit(1)
}

console.log(`Uploading ${files.length} file(s) to Vercel Blob...\n`)

let baseOrigin = process.env.VITE_CDN_BASE_URL?.replace(/\/$/, '') || ''
const manifest = {}

for (const { full, pathname } of files) {
  const body = fs.readFileSync(full)
  const blob = await put(pathname, body, {
    access: 'public',
    token,
    addRandomSuffix: false,
    allowOverwrite: true,
    contentType: contentTypeFor(pathname),
    cacheControlMaxAge: 60 * 60 * 24 * 30,
  })

  if (!baseOrigin) {
    baseOrigin = new URL(blob.url).origin
  }

  manifest[pathname] = blob.url
  const sizeMb = (body.length / (1024 * 1024)).toFixed(2)
  console.log(`  ✓ ${pathname} (${sizeMb} MB)`)
}

const envSnippet = [
  `# Generated by npm run vercel:blob:upload — merge into .env and .env.production`,
  `VITE_CDN_BASE_URL=${baseOrigin}`,
  '',
].join('\n')

const outEnv = path.join(root, '.env.vercel-blob')
fs.writeFileSync(outEnv, envSnippet, 'utf8')

const manifestPath = path.join(root, 'scripts', 'vercel-blob-urls.json')
fs.writeFileSync(manifestPath, JSON.stringify({ baseUrl: baseOrigin, files: manifest }, null, 2), 'utf8')

console.log(`\nDone. Public CDN base:\n  ${baseOrigin}`)
console.log(`\nExample: ${baseOrigin}/brand/logo.png`)
console.log(`\nWrote:\n  ${path.relative(root, outEnv)}`)
console.log(`  ${path.relative(root, manifestPath)}`)
console.log('\nNext: copy VITE_CDN_BASE_URL into .env.production, then npm run build')
